  function displayGroups(data) {
    const worldCupDiv = document.getElementById("world-cup");
    const worldCupbtn = document.getElementById("group-btn");

    let html = `<h2>${data[0].name}</h2>`;
    let worldCupbtns = ""; 

    data[0].groups.forEach((group, index) => {
        worldCupbtns += `<button class="btn btn-primary" onclick="showGroupModal(${index})">
                          ${group.name}
                       </button>`;
    });

    worldCupDiv.innerHTML = html;
    worldCupbtn.innerHTML = worldCupbtns;
}

  

  function showGroupModal(groupIndex) {
      const group = data[0].groups[groupIndex];
      document.getElementById("groupModalTitle").innerText = group.name;
  
      let modalContent = `<table border="1" class="table table-striped">
                              <tr>
                                  <th>Position</th>
                                  <th>Team</th>
                                  <th>Code</th>
                                  <th>Played</th>
                                  <th>Won</th>
                                  <th>Drawn</th>
                                  <th>Lost</th>
                                  <th>Goals For</th>
                                  <th>Goals Against</th>
                                  <th>Points</th>
                              </tr>`;
  
      group.standings.forEach(team => {
          modalContent += `<tr>
                              <td>${team.pos}</td>
                              <td>${team.team.name}</td>
                              <td>${team.team.code}</td>
                              <td>${team.played}</td>
                              <td>${team.won}</td>
                              <td>${team.drawn}</td>
                              <td>${team.lost}</td>
                              <td>${team.goals_for}</td>
                              <td>${team.goals_against}</td>
                              <td>${team.pts}</td>
                           </tr>`;
      });
  
      modalContent += `</table>`;
  
      document.getElementById("groupModalBody").innerHTML = modalContent;
      document.getElementById("groupModal").style.display = "block"; 
  }
  

  function closeModal() {
      document.getElementById("groupModal").style.display = "none";
  }
  
  displayGroups(data);